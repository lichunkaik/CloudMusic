package com.dongxun.lichunkai.cloudmusic.Class;

/**
 * 歌词类
 */
public class Lyric {
    private String time;//开始时间
    private String text;//歌词内容

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
